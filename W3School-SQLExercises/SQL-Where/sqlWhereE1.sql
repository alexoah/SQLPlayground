/**
 *	from SQL Where: Exercise 1 ( https://www.w3schools.com/sql/exercise.asp?filename=exercise_where1 )
 *
 *	question:
 *	Select all records where the City column has the value "Berlin".
 *
 *	SELECT * FROM Customers
 *	_____ ____ = ________;
 *
